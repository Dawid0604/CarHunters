package pl.dawid0604.carhunters.car.service.dao;

/**
 * Enum representing the type of gearbox used in a car.
 *
 * <p>
 *     This enum is used in the {@link AdvertisementEntity} to specify
 *     whether the car has a manual or automatic transmission.
 * </p>
 */
public enum GearboxType {

    /**
     * Manual gearbox.
     */
    MANUAL,

    /**
     * Automatic gearbox.
     */
    AUTOMATIC
}
