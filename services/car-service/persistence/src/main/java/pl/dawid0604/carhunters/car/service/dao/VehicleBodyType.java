package pl.dawid0604.carhunters.car.service.dao;

/**
 * Enum representing the type of vehicle body.
 *
 * <p>
 *     This enum is used in the {@link AdvertisementEntity} to
 *     specify which type of vehicle is used.
 * </p>
 */
public enum VehicleBodyType {

    /**
     * Cabriolet.
     */
    CABRIOLET,

    /**
     * Sedan.
     */
    SEDAN,

    /**
     * Coupe.
     */
    COUPE,

    /**
     * Hatchback.
     */
    HATCHBACK,

    /**
     * Kombi.
     */
    KOMBI,

    /**
     * Off-road.
     */
    OFF_ROAD,

    /**
     * Mini-van.
     */
    MINI_VAN,

    /**
     * SUV.
     */
    SUV
}
